						<h2>Índice de Acceso a la Información, IDAIM</h2>
						<p>El IDAIM es un índice que mide la calidad de las leyes de transparencia y acceso a la información en México con relación a las mejores prácticas nacionales e internacionales. Se divide en tres variables:</p>
						<ul>
							<li>Disposiciones normativas,</li>
							<li>Diseño institucional, y</li>
							<li>Procedimiento de acceso a la información y obligaciones de transparencia.</li>
						</ul>
						<p>El IDAIM tiene como objeto mostrar las fortalezas y deficiencias de los marcos normativos en materia de transparencia en México con la finalidad de mejorarlos y con ello avanzar en la garantía del derecho de acceso a la información.</p>
						<p>La primera versión de este índice se realizó en 2010, los resultados pueden encontrarse en: <br />
						<a href="http://www.checatuley.org">www.checatuley.org</a></p>
